# Dispatch
# ---------------------------------------------------------------------------
def _smtp_config_from_env() -> Optional[dict]:
    """Pick up SMTP settings from env vars (the same pattern as HF_TOKEN).

    All-or-nothing: missing host / port / sender returns None and the
    email channel is skipped. Keeps configuration explicit — we never
    silently fall through to a localhost smtpd nobody asked for.
    """
    host = os.getenv("ESG_SMTP_HOST")
    sender = os.getenv("ESG_SMTP_SENDER")
    port = os.getenv("ESG_SMTP_PORT", "587")
    if not host or not sender:
        return None
    try:
        port_i = int(port)
    except ValueError:
        return None
    return {
        "host": host,
        "port": port_i,
        "username": os.getenv("ESG_SMTP_USER", ""),
        "password": os.getenv("ESG_SMTP_PASS", ""),
        "sender": sender,
        "use_tls": os.getenv("ESG_SMTP_TLS", "1") not in ("0", "false", "False"),
    }


def _dispatch_to(route: Route, event: Event) -> DispatchResult:
    """Send ``event`` to a single ``route``, capturing any error.

    Each transport has its own try/except so one bad route can't block
    a different route in the same fan-out. The result detail string is
    short enough to render inline in the route table.
    """
    try:
        if route.channel == "email":
            smtp = _smtp_config_from_env()
            if not smtp:
                return DispatchResult(
                    route_id=route.id, channel=route.channel, ok=False,
                    detail="No SMTP config (set ESG_SMTP_HOST/SENDER/...).",
                )
            subject, text, html = _render_email(event)
            send_email(
                **smtp,
                recipients=[route.target],
                subject=subject,
                body_text=text,
                body_html=html,
            )
            return DispatchResult(route_id=route.id, channel="email", ok=True,
                                   detail="sent")
        elif route.channel == "slack":
            send_webhook(url=route.target, payload=_render_slack(event))
            return DispatchResult(route_id=route.id, channel="slack", ok=True,
                                   detail="posted")
        elif route.channel == "teams":
            send_webhook(url=route.target, payload=_render_teams(event))
            return DispatchResult(route_id=route.id, channel="teams", ok=True,
                                   detail="posted")
        elif route.channel == "generic_webhook":
            send_webhook(url=route.target, payload=_render_generic(event))
            return DispatchResult(route_id=route.id, channel="generic_webhook",
                                   ok=True, detail="posted")
        else:
            return DispatchResult(route_id=route.id, channel=route.channel,
                                   ok=False, detail=f"Unknown channel: {route.channel}")
    except Exception as exc:  # noqa: BLE001 — best effort across all transports
        return DispatchResult(
            route_id=route.id, channel=route.channel, ok=False,
            detail=f"{type(exc).__name__}: {exc}"[:200],
        )


def notify(event: Event, *, owner: str | None = None,
           routes: list[Route] | None = None) -> list[DispatchResult]:
    """Fan-out ``event`` to every matching route. Never raises.

    Either ``owner`` (resolves to ``RouteStore``) or an explicit
    ``routes`` list must be provided. With neither, the function
    short-circuits to an empty result so callers in code paths without
    a configured owner still work.
    """
    if routes is None:
        if not owner:
            return []
        try:
            routes = get_route_store().list_routes(owner)
        except Exception as exc:  # noqa: BLE001
            _log.warning("notifications: route lookup failed: %s", exc)
            return []

    matching = [r for r in routes if r.matches(event)]
    if not matching:
        return []

    results: list[DispatchResult] = []
    for route in matching:
        results.append(_dispatch_to(route, event))
    return results


