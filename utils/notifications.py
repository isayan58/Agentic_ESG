"""Notification routing — email + Slack / Teams webhook fan-out.

Used when something happens that a human should know about even if
they don't have ESG Pilot open at that moment:

* A regulatory update lands in the approval queue
* A pipeline run completes (or fails)
* A risk threshold is breached
* A supplier submits new data via the portal

Design goals
------------
* **Best-effort by default** — sending a notification must never break
  the action that triggered it. Every transport catches its own
  exceptions and reports back through ``DispatchResult``.
* **No mandatory infrastructure** — SMTP credentials and webhook URLs
  are *optional*. With nothing configured, ``notify(...)`` returns a
  no-op result and the caller logs that fact. The platform still works.
* **Per-user routes** — each org admin configures the routes they want
  for the events they care about. Routes are stored on the same private
  HF Dataset that backs the rest of the auth/profile state.

Public surface
--------------
``Event``                  -> dataclass describing what happened
``DispatchResult``         -> per-route delivery status
``Route``                  -> a configured destination (email / webhook)
``RouteStore``             -> CRUD for a user's routes (HF or local fallback)
``send_email(...)``        -> low-level SMTP send (raises on failure)
``send_webhook(...)``      -> low-level POST (raises on failure)
``notify(event, ...)``     -> fan-out to all matching routes (best-effort)
"""
from __future__ import annotations

import io
import json
import logging
import os
import smtplib
import ssl
import threading
import time
import uuid
from dataclasses import dataclass, field, asdict
from datetime import datetime, timezone
from email.message import EmailMessage
from pathlib import Path
from typing import Iterable, Optional

import requests

_log = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Event taxonomy
# ---------------------------------------------------------------------------
EVENT_TYPES: tuple[str, ...] = (
    "regulatory_update_pending",
    "pipeline_run_completed",
    "pipeline_run_failed",
    "risk_threshold_breached",
    "supplier_submission_received",
    "test",
)

CHANNELS: tuple[str, ...] = ("email", "slack", "teams", "generic_webhook")


@dataclass
class Event:
    """Plain envelope passed to ``notify(...)``.

    Stays a simple dataclass on purpose — no template engine, no
    pluggable formatters. Each transport renders the event itself in a
    way that suits its medium (HTML for email, blocks for Slack, etc.).
    """
    type: str
    title: str
    summary: str
    severity: str = "info"            # info | warning | critical
    org_id: str | None = None
    actor: str | None = None
    payload: dict = field(default_factory=dict)
    url: str | None = None            # link the recipient should open

    def __post_init__(self) -> None:
        if self.type not in EVENT_TYPES:
            # Don't crash here — emit a warning and pass through. A
            # call site adding a new event type shouldn't be blocked
            # while EVENT_TYPES catches up.
            _log.warning("notifications: unknown event type %r", self.type)


@dataclass
class DispatchResult:
    """Per-route delivery outcome returned by ``notify``."""
    route_id: str
    channel: str
    ok: bool
    detail: str = ""


@dataclass
class Route:
    """A configured destination + the events it cares about.

    ``id`` is autogenerated when missing — UI code shouldn't have to
    invent stable identifiers. ``filters.event_types`` is the empty
    list to mean "all events" so a fresh route just receives everything
    by default.
    """
    id: str
    channel: str                      # one of CHANNELS
    label: str
    target: str                       # email address or webhook URL
    enabled: bool = True
    event_types: list[str] = field(default_factory=list)
    min_severity: str = "info"        # info < warning < critical
    extra: dict = field(default_factory=dict)
    created_at: str = ""

    @staticmethod
    def new(channel: str, label: str, target: str, **kwargs) -> "Route":
        return Route(
            id=uuid.uuid4().hex[:12],
            channel=channel,
            label=label,
            target=target,
            created_at=datetime.now(timezone.utc).isoformat(timespec="seconds"),
            **kwargs,
        )

    def matches(self, event: Event) -> bool:
        if not self.enabled:
            return False
        if self.event_types and event.type not in self.event_types:
            return False
        return _severity_rank(event.severity) >= _severity_rank(self.min_severity)


_SEVERITY_ORDER = {"info": 0, "warning": 1, "critical": 2}


def _severity_rank(severity: str) -> int:
    return _SEVERITY_ORDER.get((severity or "info").lower(), 0)


# ---------------------------------------------------------------------------
# Transports
# ---------------------------------------------------------------------------
def send_email(*, host: str, port: int, username: str, password: str,
               sender: str, recipients: Iterable[str],
               subject: str, body_text: str,
               body_html: str | None = None,
               use_tls: bool = True,
               timeout: float = 15.0) -> None:
    """Send a single email via SMTP. Raises on any failure.

    Kept transport-only on purpose — call sites compose the message and
    decide whether to swallow exceptions. No retries here; the caller
    holds the policy.
    """
    msg = EmailMessage()
    msg["From"] = sender
    msg["To"] = ", ".join(list(recipients))
    msg["Subject"] = subject
    msg.set_content(body_text)
    if body_html:
        msg.add_alternative(body_html, subtype="html")

    if use_tls:
        context = ssl.create_default_context()
        with smtplib.SMTP(host, port, timeout=timeout) as server:
            server.starttls(context=context)
            if username:
                server.login(username, password)
            server.send_message(msg)
    else:
        with smtplib.SMTP(host, port, timeout=timeout) as server:
            if username:
                server.login(username, password)
            server.send_message(msg)


def send_webhook(*, url: str, payload: dict,
                 headers: dict | None = None,
                 timeout: float = 10.0) -> None:
    """POST ``payload`` to ``url`` as JSON. Raises on non-2xx or transport error."""
    headers = {"Content-Type": "application/json", **(headers or {})}
    response = requests.post(url, json=payload, headers=headers, timeout=timeout)
    response.raise_for_status()


